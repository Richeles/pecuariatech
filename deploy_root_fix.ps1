    Copy-Item -Path "$ProjectPath\app" -Destination $BackupPath -Recurse -Force
    Log-Info "‚úÖ Backup da pasta app criado em: $BackupPath"
}

# Corrigir p√°gina raiz / redirecionamento para /dashboard
